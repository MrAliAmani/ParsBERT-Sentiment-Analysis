def y_loss(y_true, y_pred, losses):
  """
  Args:
    y_true: The ground truth labels.
    y_pred: The predicted labels.
    losses: The losses.

  Returns:
    The ground truth labels, predicted labels, and mean loss.
  """

  y_true = torch.stack(y_true).cpu().detach().numpy()
  y_pred = torch.stack(y_pred).cpu().detach().numpy()
  y = [y_true, y_pred]
  loss = np.mean(losses)

  return y, loss


def eval_op(model, data_loader, loss_fn):
  """
  Args:
    model: The model to be evaluated.
    data_loader: The data loader to use for evaluation.
    loss_fn: The loss function to use for evaluation.

  Returns:
    The ground truth labels, predicted labels, and mean loss.
  """

  model.eval()

  losses = []
  y_pred = []
  y_true = []

  with torch.no_grad():
    for dl in tqdm(data_loader, total=len(data_loader), desc="Evaluation... "):

      input_ids = dl['input_ids']
      attention_mask = dl['attention_mask']
      token_type_ids = dl['token_type_ids']
      targets = dl['targets']

      # move tensors to GPU if CUDA is available
      input_ids = input_ids.to(device)
      attention_mask = attention_mask.to(device)
      token_type_ids = token_type_ids.to(device)
      targets = targets.to(device)

      # compute predicted outputs by passing inputs to the model
      outputs = model(
          input_ids=input_ids,
          attention_mask=attention_mask,
          token_type_ids=token_type_ids)

      # convert output probabilities to predicted class
      _, preds = torch.max(outputs, dim=1)

      # calculate the batch loss
      loss = loss_fn(outputs, targets)

      # accumulate all the losses
      losses.append(loss.item())

      y_pred.extend(preds)
      y_true.extend(targets)

  eval_y, eval_loss = y_loss(y_true, y_pred, losses)
  return eval_y, eval_loss


def train_op(model,
              data_loader,
              loss_fn,
              optimizer,
              scheduler,
              step=0,
              print_every_step=100,
              eval=False,
              eval_cb=None,
              eval_loss_min=np.Inf,
              eval_data_loader=None,
              clip=0.0):

  """
  Args:
    model: The model to be trained.
    data_loader: The data loader to use for training.
    loss_fn: The loss function to use for training.
    optimizer: The optimizer to use for training.
    scheduler: The scheduler to use for training.
    step: The current step.
    print_every_step: The number of steps between printing training information.
    eval: Whether to evaluate the model after each training step.
    eval_cb: A callback function to be called after each evaluation.
    eval_loss_min: The minimum evaluation loss so far.
    eval_data_loader: The data loader to use for evaluation.
    clip: The gradient clipping value.

  Returns:
    The ground truth labels, predicted labels, current step, and minimum evaluation loss.
  """

  model.train()

  losses = []
  y_pred = []
  y_true = []

  for dl in tqdm(data_loader, total=len(data_loader), desc="Training... "):
    step += 1

    input_ids = dl['input_ids']
    attention_mask = dl['attention_mask']
    token_type_ids = dl['token_type_ids']
    targets = dl['targets']

    # move tensors to GPU if CUDA is available
    input_ids = input_ids.to(device)
    attention_mask = attention_mask.to(device)
    token_type_ids = token_type_ids.to(device)
    targets = targets.to(device)

    # clear the gradients of all optimized variables
    optimizer.zero_grad()

    # compute predicted outputs by passing inputs to the model
    outputs = model(
        input_ids=input_ids,
        attention_mask=attention_mask,
        token_type_ids=token_type_ids)

    # convert output probabilities to predicted class
    _, preds = torch.max(outputs, dim=1)

    # calculate the batch loss
    loss = loss_fn(outputs, targets)

    # accumulate all the losses
    losses.append(loss.item())

    # compute gradient of the loss with respect to model parameters
    loss.backward()

    # `clip_grad_norm` helps prevent the exploding gradient problem in RNNs / LSTMs.
    if clip > 0.0:
      nn.utils.clip_grad_norm_(model.parameters(), max_norm=clip)

    # perform optimization step
    optimizer.step()

    # perform scheduler step
    scheduler.step()

    y_pred.extend(preds)
    y_true.extend(targets)

    if eval:
      train_y, train_loss = y_loss(y_true, y_pred, losses)
      train_score = acc_and_f1(train_y[0], train_y[1], average='weighted')

      if step % print_every_step == 0:
        eval_y, eval_loss = eval_op(model, eval_data_loader, loss_fn)
        eval_score = acc_and_f1(eval_y[0], eval_y[1], average='weighted')

        if hasattr(eval_cb, '__call__'):
          eval_loss_min = eval_cb(model, step, train_score, train_loss, eval_score, eval_loss, eval_loss_min)

  train_y, train_loss = y_loss(y_true, y_pred, losses)

  return train_y, train_loss, step, eval_loss_min

