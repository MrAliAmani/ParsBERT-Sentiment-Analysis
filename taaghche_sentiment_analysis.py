# This class is used to represent a single example for simple sequence classification. It has the following attributes:

# guid: A unique identifier for the example.
# text_a: The first input text.
# text_b: The second input text (optional).
# label: The label for the example.
class InputExample:
  """ A single example for simple sequence classification. """

  def __init__(self, guid, text_a, text_b=None, label=None):
    """ Constructs a InputExample. """

    self.guid = guid
    self.text_a = text_a
    self.text_b = text_b
    self.label = label


# This function creates examples for the training and validation datasets. It takes the following arguments:

# tokenizer: A BERT tokenizer.
# x: A list of input texts.
# y: A list of labels (optional).
# maxlen: The maximum sequence length.
# output_mode: The output mode (either "classification" or "regression").
# is_tf_dataset: Whether to return a TensorFlow dataset or a list of NumPy arrays.
def make_examples(tokenizer, x, y=None, maxlen=128, output_mode="classification", is_tf_dataset=True):
  """Creates examples for the training and validation datasets."""

  # convert the list of InputExample objects to a list of Features objects.
  examples = []
  y = y if isinstance(y, list) or isinstance(y, np.ndarray) else [None] * len(x)

  for i, (_x, _y) in tqdm(enumerate(zip(x, y)), position=0, total=len(x)):
    guid = "%s" % i
    label = int(_y)

    if isinstance(_x, str):
      text_a = _x
      text_b = None
    else:
      assert len(_x) == 2
      text_a = _x[0]
      text_b = _x[1]

    examples.append(InputExample(guid=guid, text_a=text_a, text_b=text_b, label=label))

  # The function then converts the list of Features objects to a TensorFlow dataset or a list of NumPy arrays.
  features = glue_convert_examples_to_features(
      examples,
      tokenizer,
      maxlen,
      output_mode=output_mode,
      label_list=list(np.unique(y)))

  all_input_ids = []
  all_attention_masks = []
  all_token_type_ids = []
  all_labels = []

  for f in tqdm(features, position=0, total=len(examples)):
    if is_tf_dataset:
      all_input_ids.append(tf.constant(f.input_ids))
      all_attention_masks.append(tf.constant(f.attention_mask))
      all_token_type_ids.append(tf.constant(f.token_type_ids))
      all_labels.append(tf.constant(f.label))
    else:
      all_input_ids.append(f.input_ids)
      all_attention_masks.append(f.attention_mask)
      all_token_type_ids.append(f.token_type_ids)
      all_labels.append(f.label)

  if is_tf_dataset:
    dataset = tf.data.Dataset.from_tensor_slices(({
        'input_ids': all_input_ids,
        'attention_mask': all_attention_masks,
        'token_type_ids': all_token_type_ids
    }, all_labels))

    return dataset, features

  xdata = [np.array(all_input_ids), np.array(all_attention_masks), np.array(all_token_type_ids)]
  ydata = all_labels

  return [xdata, ydata], features

