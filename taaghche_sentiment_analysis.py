class TaaghcheDataset(torch.utils.data.Dataset):
  """ Create a PyTorch dataset for Taaghche. """

  def __init__(self, tokenizer, comments, targets=None, label_list=None, max_len=128):
    """
    Args:
      tokenizer: A tokenizer object.
      comments: A list of comments.
      targets: A list of targets.
      label_list: A list of labels.
      max_len: The maximum length of a tokenized input sequence.
    """

    self.comments = comments
    self.targets = targets
    self.has_target = isinstance(targets, list) or isinstance(targets, np.ndarray)

    self.tokenizer = tokenizer
    self.max_len = max_len

    self.label_map = {label: i for i, label in enumerate(label_list)} if isinstance(label_list, list) else {}

  def __len__(self):
    """
    Returns:
      The length of the dataset.
    """

    return len(self.comments)

  def __getitem__(self, item):
    """
    Args:
      item: The index of the item to be returned.

    Returns:
      A dictionary containing the following keys:
        * comment: The comment at the given index.
        * input_ids: The token IDs of the comment.
        * attention_mask: The attention mask for the comment.
        * token_type_ids: The token type IDs for the comment.
        * targets: The target for the comment (if available).
    """

    comment = str(self.comments[item])

    if self.has_target:
      target = self.label_map.get(str(self.targets[item]), str(self.targets[item]))

    encoding = self.tokenizer.encode_plus(
        comment,
        add_special_tokens=True,
        truncation=True,
        max_length=self.max_len,
        return_token_type_ids=True,
        padding='max_length',
        return_attention_mask=True,
        return_tensors='pt')

    inputs = {
        'comment': comment,
        'input_ids': encoding['input_ids'].flatten(),
        'attention_mask': encoding['attention_mask'].flatten(),
        'token_type_ids': encoding['token_type_ids'].flatten(),
    }

    if self.has_target:
      inputs['targets'] = torch.tensor(target, dtype=torch.long)

    return inputs

