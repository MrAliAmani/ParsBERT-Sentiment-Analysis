def predict(model, comments, tokenizer, max_len=128, batch_size=32):
  """
  Predicts the labels for a given set of comments.

  Args:
    model: The PyTorch model to use for prediction.
    comments: A list of comments to predict labels for.
    tokenizer: A tokenizer to use for tokenizing the comments.
    max_len: The maximum length of the tokenized comments.
    batch_size: The batch size to use for prediction.

  Returns:
    predictions: A numpy array of the predicted labels.
    prediction_probs: A numpy array of the predicted class probabilities.
  """

  data_loader = create_data_loader(comments, None, tokenizer, max_len, batch_size, None)

  predictions = []
  prediction_probs = []


  model.eval()
  with torch.no_grad():
    for dl in tqdm(data_loader, position=0):
      input_ids = dl['input_ids']
      attention_mask = dl['attention_mask']
      token_type_ids = dl['token_type_ids']

      # move tensors to GPU if CUDA is available
      input_ids = input_ids.to(device)
      attention_mask = attention_mask.to(device)
      token_type_ids = token_type_ids.to(device)

      # compute predicted outputs by passing inputs to the model
      outputs = model(
          input_ids=input_ids,
          attention_mask=attention_mask,
          token_type_ids=token_type_ids)

      # convert output probabilities to predicted class
      _, preds = torch.max(outputs, dim=1)

      predictions.extend(preds)
      prediction_probs.extend(F.softmax(outputs, dim=1))

  predictions = torch.stack(predictions).cpu().detach().numpy()
  prediction_probs = torch.stack(prediction_probs).cpu().detach().numpy()

  return predictions, prediction_probs

