def get_training_dataset(dataset, batch_size):
  """Creates a training dataset from the given dataset and batch size.

  Args:
    dataset: A TensorFlow dataset.
    batch_size: The batch size.

  Returns:
    A TensorFlow dataset that is repeated, shuffled, and batched.
  """

  dataset = dataset.repeat()
  dataset = dataset.shuffle(2048)
  dataset = dataset.batch(batch_size)

  return dataset


def get_validation_dataset(dataset, batch_size):
  """Creates a validation dataset from the given dataset and batch size.

  Args:
    dataset: A TensorFlow dataset.
    batch_size: The batch size.

  Returns:
    A TensorFlow dataset that is batched.
  """

  dataset = dataset.batch(batch_size)

  return dataset

